<?php

/**
 * Конфигурация ограничений частоты запросов (Rate Limiting)
 */
return [
    'enabled' => true, // Глобальный переключатель подсистемы

    // Настройки лимитов: [Количество разрешенных запросов, Окно времени в секундах]
    'rules' => [
        // Глобальный лимит на любые просмотры страниц (защита от парсеров и DDoS)
        'global.get' => [
            'max_requests' => 100,
            'window'       => 60 // 100 кликов в минуту
        ],
        
        // Жесткий лимит на отправку любых POST-форм (защита от спама и брутфорса)
        'global.post' => [
            'max_requests' => 15,
            'window'       => 60 // Максимум 15 отправка форм в минуту
        ],

        // Сверхстрогий лимит конкретно на форму входа и регистрации
        'auth.submit' => [
            'max_requests' => 5,
            'window'       => 60 // Всего 5 попыток ввода пароля в минуту
        ]
    ]
];
