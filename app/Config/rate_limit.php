<?php

/**
 * Конфигурация Rate Limiter (ограничитель частоты запросов)
 * 
 * Назначение:
 * - Защита от флуда и DDoS-атак
 * - Ограничение количества запросов с одного IP
 * - Особые правила для критичных маршрутов (авторизация, регистрация)
 * 
 * Как работает:
 * - Каждый запрос пользователя логируется в БД
 * - Если количество запросов за окно времени превышает лимит — блокировка (HTTP 429)
 * - Блокировка снимается автоматически после истечения окна времени
 * 
 * Примечание:
 * - Rate limiter применяется ДО проверки маршрутов
 * - Доверенные IP (из config.app.trusted_ips) могут быть исключены в IpResolver
 */

return [
    /**
     * Глобальное включение/отключение rate limiter
     * 
     * false — полностью отключает защиту (удобно для отладки)
     * true  — включает проверку лимитов для всех запросов
     */
    'enabled' => true,

    /**
     * Вероятность запуска Garbage Collection (очистки старых записей)
     * 
     * Значение от 1 до 100 (проценты):
     * - 5   = 5% шанс очистки на каждом запросе (рекомендуется)
     * - 100 = очистка на каждом запросе (медленно, но всегда чисто)
     * - 0   = никогда не очищать (нужен cron для очистки)
     * 
     * Очистка удаляет записи старше максимального window из конфига
     */
    'gc_probability' => 5,

    /**
     * Правила ограничения запросов
     * 
     * Структура каждого правила:
     * - enabled       (bool)   Включено ли это правило
     * - max_requests  (int)    Максимальное количество запросов
     * - window        (int)    Окно времени в секундах (60 = 1 минута)
     * - routes        (array)  Список URI, к которым применяется правило (опционально)
     * 
     * Формула: max_requests запросов за window секунд с одного IP
     */
    'rules' => [
        
        /**
         * Правило для форм авторизации/регистрации
         * 
         * Применяется к: /login, /register, /password/reset
         * Лимит: 5 запросов в минуту с одного IP
         * 
         * Зачем: защита от брутфорса паролей и массовых регистраций
         */
        'auth.submit' => [
            'enabled' => true,
            'max_requests' => 5,
            'window' => 60,
            'routes' => ['/login', '/register', '/password/reset']
        ],

        /**
         * Глобальное правило для всех POST-запросов
         * 
         * Применяется ко всем POST-запросам, НЕ попавшим в auth.submit
         * Лимит: 30 запросов в минуту с одного IP
         * 
         * Зачем: защита от флуда формами (комментарии, сообщения, голосования)
         */
        'global.post' => [
            'enabled' => true,
            'max_requests' => 30,
            'window' => 60
        ],

        /**
         * Глобальное правило для всех GET-запросов
         * 
         * Применяется ко всем GET-запросам
         * Лимит: 100 запросов в минуту с одного IP
         * 
         * Зачем: защита от скрапинга и агрессивного обхода сайта
         * Примечание: лимит достаточно высокий, чтобы не мешать обычным пользователям
         */
        'global.get' => [
            'enabled' => true,
            'max_requests' => 100,
            'window' => 60
        ],

        /**
         * Пример дополнительного правила для API
         * 
         * Можно добавить свои правила для конкретных действий:
         * 
         * 'api.upload' => [
         *     'enabled' => true,
         *     'max_requests' => 10,
         *     'window' => 60,
         *     'routes' => ['/api/upload', '/api/import']
         * ],
         * 
         * 'api.search' => [
         *     'enabled' => true,
         *     'max_requests' => 20,
         *     'window' => 60,
         *     'routes' => ['/api/search']
         * ],
         */
    ]
];