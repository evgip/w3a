    <h2>💡 Философия проекта</h2>
    
    <p>
        <strong>Проект W3A объединяет в себе сильные инженерные идеи:</strong>
        текстовый минимализм
        <a href="https://github.com/lobsters/lobsters" target="_blank" rel="noopener noreferrer">Lobsters</a>
        и <a href="https://news.ycombinator.com/" target="_blank" rel="noopener noreferrer">Hacker News</a>,
        бескомпромиссную прямоту <em>Джейсона Найта</em> и философию чистого кода,
        простоту и скорость фреймворка
        <a href="https://hleb2framework.ru/" target="_blank" rel="noopener noreferrer">HLEB2</a>.
    </p>

    <p class="hint">
        В основе концепции лежит борьба со «сложностью ради сложности» (overengineering).
        Построенный по принципу максимальной самодостаточности форум имеет огромный потенциал
        стать быстрым, независимым и по-настоящему чистым продуктом.
    </p>

    <hr>

    <h2>🏛️ Основные столпы проекта</h2>

    <h3>⚡ Борьба с оверинжинирингом</h3>
    <p class="hint">Влияние Джейсона Найта</p>

    <blockquote>
        <p>
            «Делать больше работы с большим количеством кода более сложным образом
            каким-то волшебным образом "легче", или "проще", или "лучше", чем ванильные эквиваленты» —
            это ложь.
        </p>
    </blockquote>

    <p>
        Разработчики часто тратят драгоценное время на настройку абстракций, сборщиков
        и тяжёлых JS-фреймворков там, где достаточно обычного HTML и структурированного CSS.
    </p>

    <ul>
        <li>
            <strong>Принцип W3A</strong> (World Wide Web Ascetic / Accessibility):
            категорический отказ от «магических абстракций».
        </li>
        <li>
            <strong>Vanilla-подход:</strong> быстрая загрузка, семантическая вёрстка,
            минимальный чистый JS и абсолютный фокус на контенте.
        </li>
    </ul>

    <h3>🧩 Самодостаточность</h3>
    <p class="hint">Архитектурные уроки HLEB2</p>

    <p>
        W3A не использует чужие фреймворки и пишется с нуля, но архитектурные решения
        HLEB2 дали важные ориентиры для создания независимого ядра:
    </p>

    <ul>
        <li>
            <strong>Minimal dependencies:</strong> можно писать эффективный софт
            без раздутого каталога сторонних библиотек. Вы контролируете 100% кодовой базы.
        </li>
        <li>
            <strong>Лаконичность ядра:</strong> каждый компонент (роутер, шаблонизатор,
            работа с БД) выполняет только свою задачу.
        </li>
        <li>
            <strong>Максимальная производительность:</strong> чистый софт без лишних
            слоёв обработки работает быстрее любых существующих фреймворков.
        </li>
    </ul>

    <h3>👥 Структура сообщества</h3>
    <p class="hint">Логика Lobsters и Hacker News</p>

    <p>
        Архитектура Lobsters хороша тем, что она социальная, а не просто техническая:
    </p>

    <ul>
        <li>
            <strong>Теги вместо подфорумов:</strong> жёстко заданные теги работают
            эффективнее, чем миллион пустых и заброшенных категорий.
        </li>
        <li>
            <strong>Древовидные комментарии:</strong> позволяют вести глубокие,
            структурированные дискуссии без линейного хаоса.
        </li>
        <li>
            <strong>Прозрачность модерации:</strong> публичный лог действий администрации
            предотвращает токсичность и кумовство.
        </li>
    </ul>

