    <h2>🛠️ Технологический стек W3A</h2>

    <p class="hint">
        Поскольку W3A полностью автономен, его кодовая база — чистое воплощение «Vanilla Web»:
    </p>

    <h3>🐘 Бэкенд</h3>
    <p>
        Чистый, структурированный PHP с минимальными зависимостями.
        Собственный быстрый роутинг и легковесные контроллеры.
    </p>

    <h3>🗄️ База данных</h3>
    <p>
        Прямые, оптимизированные SQL-запросы без тяжёлых ORM,
        съедающих память.
    </p>

    <h3>🎨 Фронтенд</h3>
    <p>
        Семантический HTML5 и нативный CSS (Flexbox/Grid),
        написанные вручную.
    </p>

