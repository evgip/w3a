<?php
/**
 * Маршруты модуля Moderations (инструменты модераторов)
 * 
 * Все маршруты требуют роль модератора или администратора.
 * Админы имеют доступ автоматически благодаря иерархии ролей.
 * 
 * @var \App\Core\Router $router
 */

use App\Modules\Moderations\Controllers\ModerationsController;

// =========================================================================
// МАРШРУТЫ ДЛЯ МОДЕРАТОРОВ И АДМИНОВ (префикс /mod)
// =========================================================================

$router->group(['middleware' => ['web', 'moderator'], 'prefix' => '/mod'], function($router) {
    
    // -------------------------------------------------------------------------
    // ПУБЛИЧНЫЙ ЛОГ МОДЕРАЦИИ
    // -------------------------------------------------------------------------
    
    /**
     * Публичный лог всех модерационных действий.
     * Доступен модераторам и админам для прозрачности.
     */
    $router->add(
        'GET', 
        '/log', 
        ModerationsController::class . '@log', 
        'mod.log'
    );
    
    // -------------------------------------------------------------------------
    // ПРИВАТНЫЕ ЗАМЕТКИ О ПОЛЬЗОВАТЕЛЯХ
    // -------------------------------------------------------------------------
    
    /**
     * Список заметок модератора о пользователях.
     * Видны только автору заметок и админам.
     */
    $router->add(
        'GET', 
        '/notes', 
        ModerationsController::class . '@notes', 
        'mod.notes'
    );
    
    /**
     * Создание новой заметки о пользователе.
     */
    $router->add(
        'POST', 
        '/notes/store', 
        ModerationsController::class . '@storeNote', 
        'mod.notes.store'
    );
    
    /**
     * Удаление заметки.
     * 
     * @param int $id ID заметки
     */
    $router->add(
        'POST', 
        '/notes/{id}/delete', 
        ModerationsController::class . '@deleteNote', 
        'mod.notes.delete'
    );
    
    // -------------------------------------------------------------------------
    // СТАТИСТИКА И ДЕЙСТВИЯ
    // -------------------------------------------------------------------------
    
    /**
     * Статистика активности модераторов.
     * Показывает количество действий за период.
     */
    $router->add(
        'GET', 
        '/stats', 
        ModerationsController::class . '@stats', 
        'mod.stats'
    );
    
    /**
     * Блокировка пользователя (бан).
     * 
     * @param int $id ID пользователя для блокировки
     */
    $router->add(
        'POST', 
        '/ban/{id}', 
        ModerationsController::class . '@banUser', 
        'mod.ban'
    );
    
    // -------------------------------------------------------------------------
    // ПРЕДЛОЖЕНИЯ ИЗМЕНЕНИЙ КОНТЕНТА
    // -------------------------------------------------------------------------
    
    /**
     * Список активных предложений от пользователей.
     * Модераторы могут одобрять или отклонять их.
     */
    $router->add(
        'GET', 
        '/suggestions', 
        ModerationsController::class . '@suggestions', 
        'mod.suggestions'
    );
    
    /**
     * Одобрение предложения.
     * Изменения применяются немедленно, предложение удаляется.
     * 
     * @param int $id ID предложения
     */
    $router->add(
        'POST', 
        '/suggestions/{id}/approve', 
        ModerationsController::class . '@approveSuggestion', 
        'mod.suggestions.approve'
    );
    
    /**
     * Отклонение предложения.
     * Предложение удаляется без применения изменений.
     * 
     * @param int $id ID предложения
     */
    $router->add(
        'POST', 
        '/suggestions/{id}/reject', 
        ModerationsController::class . '@rejectSuggestion', 
        'mod.suggestions.reject'
    );
});